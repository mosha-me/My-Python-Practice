# কিন্তু আমরা এফ-স্ট্রিং বা মেথড ব্যবহার করে স্ট্রিং এবং সংখ্যা একত্রিত করতে পারি format()!

# এফ-স্ট্রিং
# পাইথন ৩.৬-এ এফ-স্ট্রিং চালু করা হয়েছিল এবং বর্তমানে এটি স্ট্রিং ফরম্যাট করার সবচেয়ে পছন্দের উপায়।

# কোনো স্ট্রিংকে এফ-স্ট্রিং হিসেবে নির্দিষ্ট করতে, fস্ট্রিং লিটারেলটির সামনে একটি `#` বসান এবং {}ভেরিয়েবল ও অন্যান্য অপারেশনের জন্য প্লেসহোল্ডার হিসেবে কার্লি ব্র্যাকেট যোগ করুন।

# উদাহরণ:
#একটি f-স্ট্রিং তৈরি করুন:

age = 36
txt = f"My name is John, I am {age}"
print(txt)

# প্লেসহোল্ডার এবং মডিফায়ার
# মানটিকে ফরম্যাট করার জন্য একটি প্লেসহোল্ডারে ভেরিয়েবল, অপারেশন, ফাংশন এবং মডিফায়ার থাকতে পারে।

# উদাহরণ:
# ভেরিয়েবলের জন্য একটি প্লেসহোল্ডার যোগ করুন price:

price = 59
txt = f"The price is {price} dollars"
print(txt)

# মানটিকে ফরম্যাট করার জন্য একটি প্লেসহোল্ডারে মডিফায়ার অন্তর্ভুক্ত করা যেতে পারে।

# একটি মডিফায়ার অন্তর্ভুক্ত করতে একটি কোলন এবং :তার পরে উপযুক্ত ফরম্যাটিং টাইপ যোগ করতে হয়, যেমন .2fযার অর্থ হলো ২ দশমিক স্থানসহ ফিক্সড পয়েন্ট সংখ্যা:

# উদাহরণ:
# দামটি দুই দশমিক স্থান পর্যন্ত প্রদর্শন করুন:

price = 59
txt = f"The price is {price:.2f} dollars"
print(txt)

# পাইথনে {price:.2f} এর মানে হলো আপনি সংখ্যাটিকে একটি Float (দশমিক সংখ্যা) হিসেবে দেখাতে চান এবং দশমিকের পরে ঠিক ২ ঘর রাখতে চান।
# এখানে প্রতিটি অংশের কাজ নিচে দেওয়া হলো:
# (কোলন): এটি ফরম্যাটিং শুরু করার সংকেত।
# .2: এটি নির্দেশ করে যে দশমিকের পরে আপনি কয়টি ঘর দেখতে চান। এখানে 2 মানে দশমিকের পর দুই ঘর।
# f: এটি 'Fixed-point' বা দশমিক সংখ্যার (Float) ফরম্যাট নির্দেশ করে।

# এটি যেভাবে কাজ করে:
# আপনার price = 59 একটি পূর্ণসংখ্যা (Integer)। কিন্তু যখন আপনি {price:.2f} ব্যবহার করেন:
# ১. পাইথন প্রথমে এটাকে দশমিকে রূপান্তর করে: 59.0000...
# ২. এরপর .2 এর কারণে দশমিকের পরের ঠিক ২ ঘর রেখে বাকিটা বাদ দিয়ে দেয়।
# ৩. ফলাফল আসে: 59.00মজার ব্যাপার:যদি price = 59.5678 হতো, তবে এটি রাউন্ডিং (Rounding) করে আউটপুট দিত: 59.57 (কারণ দশমিকের পর ৩য় ঘরটি ৫-এর বড়

# একটি প্লেসহোল্ডারে পাইথন কোড থাকতে পারে, যেমন গাণিতিক অপারেশন:

# উদাহরণ:
# প্লেসহোল্ডারে একটি গাণিতিক অপারেশন সম্পাদন করুন এবং ফলাফলটি ফেরত দিন:

txt = f"The price is {20 * 59} dollars"
print(txt)
