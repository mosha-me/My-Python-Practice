"""
পাইথনে &= অপারেটরটি হলো একটি Bitwise AND Assignment অপারেটর। এটি মূলত বিটওয়াইজ ক্যালকুলেশন এবং ভেরিয়েবলে মান জমা রাখা—এই দুটি কাজ একসাথে করে।
এখানে x &= 3 এর মানে হলো x = x & 3। এর কাজ বোঝার জন্য আপনাকে Bitwise AND (&) এবং Assignment (=) এর সমন্বয় বুঝতে হবে।

১. Bitwise AND (&) কীভাবে কাজ করে?
এটি সংখ্যাগুলোকে বাইনারিতে রূপান্তর করে প্রতি বিটে তুলনা করে। নিয়মটি হলো:যদি দুটি বিটই 1 হয়, তবে ফলাফল 1।অন্যথায় ফলাফল 0।

২. উদাহরণ দিয়ে ব্যাখ্যা:
ধরা যাক, শুরুতে আপনার x এর মান 7। এখন আপনি করলেন x &= 3।

ধাপগুলো দেখুন:

7 এর বাইনারি: 1 1 1
3 এর বাইনারি: 0 1 1
AND (&) অপারেশন:
  1 1 1  (x)
& 0 1 1  (3)
---------
  0 1 1  => যা দশমিক পদ্ধতিতে 3

Assignment (=): এখন এই নতুন মান 3 আবার x ভেরিয়েবলের মধ্যে জমা হবে। ফলে x এর ফাইনাল মান হবে 3।
"""
# ৩. কোড উদাহরণ:
x = 7
x &= 3  # এটি x = x & 3 এর সমান
print(x) # আউটপুট আসবে 3
"""
কেন এটি ব্যবহার করা হয়?
Flags বা Permissions: কোনো নির্দিষ্ট পারমিশন চেক করতে বা মুছতে এটি খুব দ্রুত কাজ করে।
Masking: কোনো সংখ্যার নির্দিষ্ট কিছু বিট রেখে বাকিগুলো ০ করে দিতে এটি ব্যবহৃত হয়।
"""
"""
------------------------------------------------------

|=
x |= 3
x = x | 3

পাইথনে |= হলো Bitwise OR Assignment অপারেটর।
এটি x = x | 3 এর একটি সংক্ষিপ্ত রূপ (Shorthand)।

এটি বুঝতে হলে আপনাকে Bitwise OR (|) এর কাজ জানতে হবে।

১. Bitwise OR (|) কীভাবে কাজ করে?
এটি সংখ্যাগুলোকে বাইনারিতে নিয়ে প্রতিটি বিট তুলনা করে। 

নিয়মটি খুব সহজ:
যদি অন্তত একটি বিট 1 হয়, তবে ফলাফল 1।
দুইটি বিটই 0 হলে ফলাফল 0।

২. উদাহরণ দিয়ে ব্যাখ্যা:
ধরা যাক, শুরুতে আপনার x এর মান 4। এখন আপনি লিখলেন x |= 3।
ধাপগুলো দেখুন:
4 এর বাইনারি: 1 0 0
3 এর বাইনারি: 0 1 1
OR (|) অপারেশন:
  1 0 0  (x = 4)
| 0 1 1  (3)
---------
  1 1 1  => যা দশমিক পদ্ধতিতে 7

Assignment (=): এই নতুন মান 7 এখন x ভেরিয়েবলে সেট হয়ে যাবে।
"""
# ৩. কোড উদাহরণ:
x = 4
x |= 3  # এটি x = x | 3 এর সমান
print(x) # আউটপুট আসবে 7
"""
কেন এটি ব্যবহার করা হয়?
Permissions যোগ করা: ডিরেক্টরি বা ডাটাবেস পারমিশনে নতুন কোনো সুবিধা (যেমন: 'Write' পারমিশন) যোগ করতে এটি ব্যবহার করা হয়।
Flags সেট করা: কোনো প্রোগ্রামে একাধিক অপশন বা ফ্ল্যাগ অন করতে এটি কার্যকর।
"""
"""
------------------------------------------------------

^=
x ^= 3
x = x ^ 3

পাইথনে ^= হলো Bitwise XOR Assignment অপারেটর। এটি x = x ^ 3 এর সংক্ষিপ্ত রূপ।
এটি বুঝতে হলে আপনাকে Bitwise XOR (^) বা "Exclusive OR" এর নিয়মটি জানতে হবে।

১. Bitwise XOR (^) কীভাবে কাজ করে?
এটি বাইনারি বিটগুলোর মধ্যে তুলনা করে এই নিয়মে:
যদি দুটি বিট আলাদা হয় (একটি ০, অন্যটি ১), তবে ফলাফল 1।
যদি দুটি বিট একই হয় (দুইটিই ০ অথবা দুইটিই ১), তবে ফলাফল 0।

২. উদাহরণ দিয়ে ব্যাখ্যা:
ধরা যাক, x এর মান 6। এখন আপনি করলেন x ^= 3।ধাপগুলো দেখুন:
6 এর বাইনারি: 1 1 0
3 এর বাইনারি: 0 1 1
XOR (^) অপারেশন: 
  1 1 0  (x = 6)
^ 0 1 1  (3)
---------
  1 0 1  => যা দশমিক পদ্ধতিতে 5
Assignment (=): এখন x এর নতুন মান হবে 5।
"""
# ৩. কোড উদাহরণ:
x = 6
x ^= 3  # এটি x = x ^ 3 এর সমান
print(x) # আউটপুট আসবে 5
"""
কেন এটি ব্যবহার করা হয়?
Toggle করা: কোনো ভ্যালুকে বারবার অন-অফ বা অদলবদল করতে এটি দারুণ।
Swap করা: থার্ড ভেরিয়েবল ছাড়াই দুটি সংখ্যার মান অদলবদল (Swap) করতে XOR ব্যবহার করা হয়।
Data Encryption: ক্রিপ্টোগ্রাফির বেসিক অ্যালগরিদমে এটি প্রচুর ব্যবহৃত হয়।
"""
"""
------------------------------------------------------

>>=
x >>= 3
x = x >> 3	

পাইথনে >>= হলো Bitwise Right Shift Assignment অপারেটর। এটি x = x >> 3 এর সংক্ষিপ্ত রূপ।

এটি মূলত একটি সংখ্যার বাইনারি বিটগুলোকে নির্দিষ্ট ঘর পর্যন্ত ডান দিকে সরিয়ে দেয়।

১. Right Shift (>>) কীভাবে কাজ করে?
সহজ কথায়, কোনো সংখ্যাকে ডান দিকে ৩ ঘর শিফট করার মানে হলো সংখ্যাটির বাইনারি থেকে ডানদিকের শেষ ৩টি বিট ফেলে দেওয়া।
গাণিতিকভাবে, x >> n মানে হলো x কে \(2^{n}\) দিয়ে ভাগ করা (এবং শুধু পূর্ণসংখ্যাটি রাখা)।

২. উদাহরণ দিয়ে ব্যাখ্যা:
ধরা যাক, x এর মান ২০। এখন আপনি করলেন x >>= 3।ধাপগুলো দেখুন:
২০ এর বাইনারি: 1 0 1 0 0
৩ ঘর ডান দিকে শিফট: ডানদিকের শেষ ৩টি বিট (1 0 0) বাদ যাবে।

1 0 1 0 0  (শুরুতে)
- - - [1 0] [1 0 0] (ডানের ৩টি বিট আউট)
ফলাফল: 1 0  (বাইনারিতে ১০ মানে দশমিকে ২)

সহজ হিসাব: ২০ কে \(2^{3}\) বা ৮ দিয়ে ভাগ করলে হয় ২.৫। যেহেতু এটি পূর্ণসংখ্যা নেয়, তাই ফলাফল ২।
"""
# ৩. কোড উদাহরণ:
