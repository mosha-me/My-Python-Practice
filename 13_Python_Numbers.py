# What is e/E:
''' পাইথনে (এবং গণিতেও) এই e বা E বলতে বোঝায় "১০ এর পাওয়ার" (Power of 10)। একে বলা হয় Scientific Notation বা বৈজ্ঞানিক সংকেত। এটি খুব বড় বা খুব ছোট সংখ্যা সহজে লেখার জন্য ব্যবহার করা হয়।
এখানে e এর পরের সংখ্যাটি নির্দেশ করে ১০ এর ওপর কত পাওয়ার আছে। চলুন আপনার উদাহরণগুলো ভেঙে দেখি:
সহজ কথায়, পাইথনে e মানে হলো '১০-এর পাওয়ার' বা 'শূন্যের সংখ্যা'।নিচের ৩টি উদাহরণ সহজভাবে দেখুন: '''
"""
১. x = 35e3:এখানে e3 মানে ১-এর পরে ৩টি শূন্য (\(10^{3}\) বা ১০০০)
হিসাব: \(35 \times 1000 = 35000.0\)

২.y = 12E4:এখানে E4 মানে ১-এর পরে ৪টি শূন্য (\(10^{4}\) বা ১০,০০০)।
হিসাব: \(12 \times 10000 = 120000.0\)

৩. z = -87.7e100:এখানে e100 মানে ১-এর পরে ১০০টি শূন্য! এত বড় সংখ্যা স্ক্রিনে লেখা কঠিন, তাই পাইথন ছোট করে দেখানোর জন্য e+101 ফরম্যাট ব্যবহার করে।
মনে রাখার সহজ নিয়ম:e এর পরে যত সংখ্যা থাকবে, দশমিক বিন্দুটি ঠিক তত ঘর ডানে সরে যাবে।এই পদ্ধতিতে লিখলে পাইথন সংখ্যাটিকে সবসময় Float (দশমিক সংখ্যা) হিসেবে ধরে নেয়, তাই শেষে একটা .0 থাকে।
"""

''' দশমিকের পরের ছোট সংখ্যার ক্ষেত্রেও নিয়মটা একই, শুধু এবার দশমিক বিন্দুটি বাম দিকে সরে যাবে।সহজভাবে বুঝে নিন: '''
"""
১. e- মানে কী?যদি e এর পরে মাইনাস (-) চিহ্ন থাকে, তবে বুঝতে হবে সংখ্যাটি ১-এর চেয়ে অনেক ছোট (ভাগ করা হচ্ছে)।

২. উদাহরণ: x = 1e-3এখানে e-3 মানে হলো ১-কে ১০০০ দিয়ে ভাগ করা (\(\frac{1}{10^{3}}\))।হিসাব: \(1 \div 1000 = 0.001\)অর্থাৎ, দশমিক বিন্দুটি ৩ ঘর বামে সরে গেল।

৩. আরেকটি উদাহরণ: y = 5.2e-4এখানে e-4 মানে দশমিক বিন্দুকে বর্তমান অবস্থান থেকে ৪ ঘর বামে সরাতে হবে।৫.২ থেকে ৪ ঘর বামে গেলে হয়: 0.00052
"""

# মনে রাখার টিপস:
''' e+ (বা শুধু e) থাকলে সংখ্যাটি বড় হবে (দশমিক ডানে সরবে)।
e- থাকলে সংখ্যাটি ছোট হবে (দশমিক বামে সরবে)। '''

# complex - জটিল:
