# টাইপ রূপান্তর:
# int()আপনি , float(), এবং পদ্ধতি ব্যবহার করে এক প্রকার থেকে অন্য প্রকারে রূপান্তর করতে পারেন complex():

# উদাহরণ:
# এক প্রকার থেকে অন্য প্রকারে রূপান্তর করুন:

x = 1    # int
y = 2.8  # float
z = 1j   # complex

#convert from int to float:
a = float(x)

#convert from float to int:
b = int(y)

#convert from int to complex:
c = complex(x)

print(a)
print(b)
print(c)

print(type(a))
print(type(b))
print(type(c))

''' দ্রষ্টব্য: আপনি অন্য যেকোনো সংখ্যা থেকে জটিল সংখ্যা রূপান্তর করা গেলেও জটিল সংখ্যাকে অন্য কোনো সংখ্যার ধরনে রূপান্তর করতে পারবেন না। '''

