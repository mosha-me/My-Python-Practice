# নেতিবাচক সূচীকরণ
"""
নেগেটিভ ইনডেক্সিং মানে শেষ থেকে শুরু করা।

-1শেষ আইটেমটিকে বোঝায়, -2শেষের আগের আইটেমটিকে বোঝায় ইত্যাদি।
"""
# উদাহরণ
