import sys

# sys মডিউলের মাধ্যমে পাইথন ভার্সন চেক করা
print("Python version info:")
print(sys.version)

# --- আমার শেখা নোটস (Notes) ---

# sys কী?
# sys হলো পাইথনের একটি বিল্ট-ইন মডিউল যার পূর্ণরূপ 'System'।
# এটি কম্পিউটার সিস্টেম এবং পাইথন ইন্টারপ্রিটারের সাথে যোগাযোগ করে।

# মডিউল (Module) কী?
# মডিউল হলো আগে থেকে লিখে রাখা কোড বা ফাংশনের একটি ফাইল।
# যা 'import' করে অন্য প্রোগ্রামে ব্যবহার করা যায়।

# ইনপুট (Input) কী?
# input() ফাংশন দিয়ে ব্যবহারকারীর কাছ থেকে তথ্য নেওয়া হয়।
name = input("আপনার নাম লিখুন: ")
print("স্বাগতম, " + name)
