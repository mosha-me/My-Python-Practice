# 1 বড় হাতের অক্ষর
# উদাহরণ:

# এই upper()মেথডটি স্ট্রিংটিকে বড় হাতের অক্ষরে রিটার্ন করে:
a = "Hello, World!"
print(a.upper())


# 2 ছোট হাতের অক্ষর
# উদাহরণ:

# এই lower()মেথডটি স্ট্রিংটিকে ছোট হাতের অক্ষরে রিটার্ন করে:
a = "Hello, World!"
print(a.lower())

# 3 ফাঁকা স্থান অপসারণ করুন
# হোয়াইটস্পেস হলো মূল লেখার আগে এবং/অথবা পরের ফাঁকা স্থান, এবং প্রায়শই এই ফাঁকা স্থানটি মুছে ফেলার প্রয়োজন হয়।
# উদাহরণ:

# এই strip()পদ্ধতিটি শুরু বা শেষের যেকোনো ফাঁকা স্থান মুছে দেয়:
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

# 4 স্ট্রিং প্রতিস্থাপন করুন
# উদাহরণ:

# এই replace()পদ্ধতিটি একটি স্ট্রিংকে অন্য একটি স্ট্রিং দিয়ে প্রতিস্থাপন করে:
a = "Hello, World!"
print(a.replace("H", "J"))

# 5  স্প্লিট স্ট্রিং
# এই split()মেথডটি একটি লিস্ট রিটার্ন করে, যেখানে নির্দিষ্ট সেপারেটরের ভেতরের টেক্সটগুলো লিস্ট আইটেম হয়ে যায়।
#উদাহরণ:

#এই split()মেথডটি স্ট্রিংটিতে বিভাজকটির উপস্থিতি খুঁজে পেলে সেটিকে সাবস্ট্রিং-এ বিভক্ত করে:
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']

