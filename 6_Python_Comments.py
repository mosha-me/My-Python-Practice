# মাল্টিলাইন মন্তব্য
# পাইথনে মাল্টিলাইন কমেন্টের জন্য নির্দিষ্ট কোনো সিনট্যাক্স নেই।
# একাধিক লাইনের মন্তব্য যোগ করতে আপনি #প্রতিটি লাইনের জন্য একটি ইনসার্ট করতে পারেন:

