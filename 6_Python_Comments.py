# উদাহরণ:
print("Hello, World!") #This is a comment

# কমেন্ট মানেই যে কোডের ব্যাখ্যা দেওয়া টেক্সট হতে হবে, এমন নয়; এটি পাইথনকে কোড এক্সিকিউট করা থেকে বিরত রাখতেও ব্যবহার করা যেতে পারে।
# উদাহরণ:
#print("Hello, World!")
