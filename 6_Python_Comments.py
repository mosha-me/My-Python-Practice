# যতক্ষণ পর্যন্ত স্ট্রিংটি কোনো ভেরিয়েবলে অ্যাসাইন করা না হয়, পাইথন কোডটি পড়বে, কিন্তু তারপর তা উপেক্ষা করবে, এবং এর মাধ্যমে আপনি একটি মাল্টিলাইন কমেন্ট তৈরি করে ফেলেছেন।
