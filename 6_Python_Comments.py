# অথবা, উদ্দেশ্য অনুযায়ী না হলেও, আপনি একটি মাল্টিলাইন স্ট্রিং ব্যবহার করতে পারেন।

# যেহেতু পাইথন ভেরিয়েবলে অ্যাসাইন করা হয়নি এমন স্ট্রিং লিটারেল উপেক্ষা করে, তাই আপনি আপনার কোডে একটি মাল্টিলাইন স্ট্রিং (ট্রিপল কোট) যোগ করতে পারেন এবং এর ভিতরে আপনার কমেন্ট রাখতে পারেন:

# উদাহরণ:
"""
This is a comment
written in
more than just one line
"""

'''
This is a comment
written in
more than just one line
'''
