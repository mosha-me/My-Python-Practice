""" পাইথন মন্তব্য:
1.পাইথন কোড ব্যাখ্যা করার জন্য কমেন্ট ব্যবহার করা যেতে পারে।
2.কোডকে আরও সহজে পাঠযোগ্য করার জন্য কমেন্ট ব্যবহার করা যেতে পারে।
3.কোড পরীক্ষা করার সময় এক্সিকিউশন আটকাতে কমেন্ট ব্যবহার করা যেতে পারে। """

# একটি মন্তব্য তৈরি করা
# যেসব কমেন্ট কমা (,) দিয়ে শুরু হয় #, পাইথন সেগুলো উপেক্ষা করবে:
# উদাহরণ:
#This is a comment
