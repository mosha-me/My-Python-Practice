# সংখ্যা মুদ্রণ করুন
# আপনি print()সংখ্যা প্রদর্শন করতেও এই ফাংশনটি ব্যবহার করতে পারেন:

# তবে, টেক্সটের মতো, আমরা সংখ্যাকে ডাবল কোটেশনের ভিতরে রাখি না:

# উদাহরণ: 
print(3)
print(358)
print(50000)

# আপনি ফাংশনের ভিতরেও গাণিতিক কাজ করতে পারেন print():

# উদাহরণ:
