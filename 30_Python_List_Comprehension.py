# উদাহরণ:

""" ফলের একটি তালিকার উপর ভিত্তি করে, আপনি এমন একটি নতুন তালিকা চান যেখানে শুধুমাত্র সেই ফলগুলো থাকবে যাদের নামে "a" অক্ষরটি রয়েছে। """

""" লিস্ট কম্প্রিহেনশন ছাড়া আপনাকে এমন একটি স্টেটমেন্ট লিখতে হবে forযার ভেতরে একটি কন্ডিশনাল টেস্ট থাকবে: """

# উদাহরণ
fruits = ["apple", "banana", "cherry", "kiwi", "mango"]
