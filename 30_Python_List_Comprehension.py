""" এক্সপ্রেশনটি হলো ইটারেশনের বর্তমান আইটেম, কিন্তু এটি ফলাফলও বটে, যা নতুন লিস্টে একটি আইটেম হিসেবে যুক্ত হওয়ার আগে আপনি পরিবর্তন করতে পারেন: """

