# উদাহরণ
# নতুন তালিকার সমস্ত মান 'hello'-তে সেট করুন:

newlist = ['hello' for x in fruits]
"""
এখানে fruits লিস্টে যতগুলো উপাদান (ফল) আছে, লুপটি ঠিক ততবারই চলেছে। প্রতিবার লুপ চলার সময় পাইথন নতুন লিস্টে 'hello' স্ট্রিংটি যোগ করেছে।
List Comprehension-এর বাম পাশের অংশটি (expression) নির্ধারণ করে নতুন লিস্টে কী জমা হবে। আপনি সেখানে লুপের ভেরিয়েবল x ব্যবহার না করে সরাসরি একটি নির্দিষ্ট শব্দ 'hello' বসিয়েছেন।
"""
"""
সহজে বোঝার জন্য নিচে ব্যাখ্যা দেওয়া হলো:
পর্দার আড়ালে যেভাবে কাজ করেছে
ধরা যাক, আপনার মূল লিস্টটি ছিল এরকম:
"""
fruits = ['apple', 'banana', 'cherry'] # এখানে ৩টি উপাদান আছ

""" আপনার কোডটি সাধারণ for লুপে লিখলে এমন দেখাতো: """
newlist = []
for x in fruits:
    newlist.append('hello') # x-এর মানের বদলে প্রতিবার 'hello' যোগ হচ্ছে
print(newlist) # আউটপুট: ['hello', 'hello', 'hello']

""" সমাধান আপনি যদি ফলের নামগুলোকেই নতুন লিস্টে রাখতে চান, তাহলে বাম পাশে 'hello'-এর জায়গায় লুপের ভেরিয়েবল x বসাতে হবে: """
# সঠিক পদ্ধতি
newlist = [x for x in fruits]
print(newlist) # আউটপুট: ['apple', 'banana', 'cherry']

""" আপনি যদি প্রতিটি ফলের নামের সাথে 'hello' শব্দটি যুক্ত করতে চান, তবে এভাবে লিখতে পারেন: """
newlist = ['hello ' + x for x in fruits]
print(newlist) # আউটপুট: ['hello apple', 'hello banana', 'hello cherry']

""" এক্সপ্রেশনটিতে শর্তও থাকতে পারে, ফিল্টারের মতো নয়, বরং ফলাফলকে প্রভাবিত করার একটি উপায় হিসেবে: """

# উদাহরণ
# 'banana'-এর পরিবর্তে 'orange' ফেরত দিন:

newlist = [x if x != "banana" else "orange" for x in fruits]

