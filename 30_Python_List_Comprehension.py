"""
Python-এ List Comprehension হলো একটি বিদ্যমান ইটারেবল (যেমন: লিস্ট, টাপল, বা রেঞ্জ) থেকে খুব সংক্ষিপ্ত এবং পরিচ্ছন্ন উপায়ে নতুন লিস্ট তৈরি করার একটি পদ্ধতি। এটি সাধারণ for লুপের একটি এক লাইনের বিকল্প, যা কোডকে আরও রিডেবল (সহজে পড়ার যোগ্য) এবং দ্রুত করে তোলে
"""
# বেসিক সিনট্যাক্স (Syntax)
new_list = [expression for item in iterable if condition]

# expression: প্রতিটি উপাদানের ওপর আপনি কী অপারেশন চালাতে চান (যেমন: গুণ করা, ফরম্যাট করা)।
# item: লুপের ভেতরের বর্তমান ভেরিয়েবল।
# iterable: মূল লিস্ট, টাপল বা রেঞ্জ যেখান থেকে ডেটা আসবে।if condition (ঐচ্ছিক): ফিল্টার করার শর্ত (শর্ত সত্য হলেই উপাদানটি নতুন লিস্টে যোগ হবে)

""" সাধারণ for লুপ বনাম List Comprehension (তুলনামূলক উদাহরণ)
ধরা যাক, ১ থেকে ৫ পর্যন্ত সংখ্যার বর্গ (Square) করে একটি নতুন লিস্ট তৈরি করতে হবে: """

# ১. ট্র্যাডিশনাল for লুপ পদ্ধতি (৪ লাইন)
squares = []
for x in range(1, 6):
    squares.append(x ** 2)
print(squares) # আউটপুট: [1, 4, 9, 16, 25]

# ২. List Comprehension পদ্ধতি (মাত্র ১ লাইন)
squares = [x ** 2 for x in range(1, 6)]
print(squares) # আউটপুট: [1, 4, 9, 16, 25]


""" শর্তসহ ব্যবহার (With if Condition)লিস্ট তৈরির সময় ডেটা ফিল্টার বা বাছাই করার জন্য এটি ব্যবহৃত হয়। যেমন, একটি লিস্ট থেকে শুধু জোড় সংখ্যাগুলো আলাদা করা: """

numbers = [1, 2, 3, 4, 5, 6]
# শুধু জোড় সংখ্যা (even numbers) ফিল্টার করা
even_numbers = [num for num in numbers if num % 2 == 0]
print(even_numbers) # আউটপুট: [2, 4, 6]

