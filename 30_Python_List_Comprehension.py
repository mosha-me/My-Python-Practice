""" কোড সংক্ষিপ্ত হয়: ৪-৫ লাইনের লুপের কাজ মাত্র এক লাইনে করা সম্ভব
পঠনযোগ্যতা বাড়ে: কোড দেখতে পরিচ্ছন্ন এবং প্রফেশনাল লাগে
গতি: এটি সাধারণ append() মেথড ব্যবহারের চেয়ে পাইথনে কিছুটা দ্রুত কাজ করে """

