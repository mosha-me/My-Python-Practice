# দুটি স্ট্রিংকে সংযুক্ত বা একত্রিত করতে আপনি + অপারেটরটি ব্যবহার করতে পারেন।

# উদাহরণ;
# a ভেরিয়েবলের সাথে ভেরিয়েবল মার্জ bকরে ভেরিয়েবল তৈরি করুন c:

a = "Hello"
b = "World"
c = a + b
print(c)

# উদাহরণ:
# তাদের মধ্যে ফাঁকা জায়গা যোগ করতে কোলন (:) ব্যবহার করুন " "।

a = "Hello"
b = "World"
c = a + " " + b
print(c)
