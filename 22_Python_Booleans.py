# বুলিয়ান দুটি মানের একটিকে বোঝায় : True।False

# বুলিয়ান মান:
''' প্রোগ্রামিংয়ে প্রায়শই কোনো এক্সপ্রেশন ইন্টিজার নাকি Trueনন- ইন্টিজার, তা জানার প্রয়োজন হয় False। '''

# আপনি পাইথনে যেকোনো এক্সপ্রেশন মূল্যায়ন করতে পারেন এবং দুটি উত্তরের মধ্যে একটি পেতে পারেন True।False

# যখন আপনি দুটি মানের তুলনা করেন, তখন এক্সপ্রেশনটি মূল্যায়ন করা হয় এবং পাইথন বুলিয়ান উত্তর ফেরত দেয়:

# উদাহরণ:
print(10 > 9)
print(10 == 9)
print(10 < 9)

# যখন আপনি if স্টেটমেন্টে কোনো শর্ত প্রয়োগ করেন, পাইথন ` Trueor` রিটার্ন করে False।

# উদাহরণ
# অবস্থাটি 'হ্যাঁ' True বা 'না' এর উপর ভিত্তি করে একটি বার্তা প্রিন্ট করুন False:

a = 200
b = 33

if b > a:
  print("b is greater than a")
else:
  print("b is not greater than a")

# সহজ কথায়, if এবং else অনেকটা রাস্তার মোড়ের মতো কাজ করে। এটি ঠিক করে যে প্রোগ্রাম কোন দিকে যাবে।
'''
১. if (যদি): এটি একটি শর্ত পরীক্ষা করে। যদি শর্তটি সত্য (True) হয়, তবে if-এর ভেতরের কোডটুকু কাজ করে। আপনার উদাহরণে, b > a (৩৩ কি ২০০-এর চেয়ে বড়?)—এই শর্তটি মিথ্যা (False)। তাই if-এর ভেতরের অংশটি কাজ করবে না।

২. else (নতুবা): যদি if-এর শর্তটি মিথ্যা হয়, তবে প্রোগ্রাম সরাসরি else-এর কাছে চলে যায় এবং এর ভেতরের কোডটুকু চালায়। অর্থাৎ, কোনো শর্ত না মিললে যা করা দরকার, তা else দিয়ে করা হয়।
'''
# আপনার কোডটি যেভাবে কাজ করছে:
"""
প্রথমে দেখছে ৩৩ কি ২০০ থেকে বড়?
যেহেতু বড় নয়, তাই if বাদ দিয়ে else-এ চলে যাচ্ছে।
আউটপুট হিসেবে দেখাচ্ছে: b is not greater than a
"""
# মান এবং চলক মূল্যায়ন করুন:

# এই bool()ফাংশনটি আপনাকে যেকোনো মান মূল্যায়ন করতে এবং বিনিময়ে আপনাকে Trueকিছু দিতে সক্ষম করে।False

# উদাহরণ:
# একটি স্ট্রিং এবং একটি সংখ্যা মূল্যায়ন করুন:

print(bool("Hello"))
print(bool(15))

# বেশিরভাগ মানই সত্য:
# প্রায় যেকোনো মানই মূল্যায়ন করা হয়, Trueযদি তার মধ্যে কোনো ধরনের বিষয়বস্তু থাকে।

# True খালি স্ট্রিং ছাড়া যেকোনো স্ট্রিংই ,।

# যেকোনো সংখ্যাই হলো True, ব্যতিক্রম 0।

# যেকোনো লিস্ট, টাপল, সেট এবং ডিকশনারি হলো True, তবে খালিগুলো নয়।

# উদাহরণ:
# নিম্নলিখিতটি True রিটার্ন করবে:

bool("abc")
bool(123)
bool(["apple", "cherry", "banana"])


# কিছু মান মিথ্যা:
# প্রকৃতপক্ষে, খালি মানগুলি ছাড়া এমন খুব বেশি মান নেই যেগুলির মান ০ হয় False, যেমন (), [], {}, "", সংখ্যা 0, এবং মান None। এবং অবশ্যই মানটির মান False০ হয় False।

# উদাহরণ:
# নিম্নলিখিতটি False রিটার্ন করবে:

bool(False)
bool(None)
bool(0)
bool("")
bool(())
bool([])
bool({})

# আরও একটি মান, বা এই ক্ষেত্রে একটি অবজেক্ট, 'true' তে মূল্যায়ন করা হয় False, এবং সেটি হলো যদি আপনার কাছে এমন একটি অবজেক্ট থাকে যা এমন একটি ক্লাস থেকে তৈরি যার একটি __len__ফাংশন 'true' 0বা ' false' রিটার্ন করে False:

# উদাহরণ
class myclass():
  def __len__(self):
    return 0

myobj = myclass()
print(bool(myobj))
'''
১. Class কি এবং এর কাজ কি?
Class হলো ডেটা এবং ফাংশন (কাজ) একসাথে রাখার একটি কাঠামো। এটি মূলত কোডকে গুছিয়ে রাখতে এবং বারবার ব্যবহার করতে সাহায্য করে।

২. এটি কিভাবে কাজ করে?
(আপনার কোডের ব্যাখ্যা)
আপনার কোডে:
class myclass(): দিয়ে আপনি একটি নকশা তৈরি করেছেন।

def __len__(self): দিয়ে আপনি বলে দিয়েছেন এই ক্লাসের কোনো কিছুর দৈর্ঘ্য (length) মাপলে সব সময় 0 দেখাবে।

myobj = myclass() এখানে আপনি নকশা (Class) থেকে একটি আসল জিনিস (Object) তৈরি করলেন।

print(bool(myobj)) যখন করলেন, পাইথন দেখল এর দৈর্ঘ্য 0। পাইথনে দৈর্ঘ্য ০ হওয়া মানে সেটি 'False'। তাই এটি False প্রিন্ট করবে।

৩. এটি কিভাবে কাজে সাহায্য করে?
কোড পুনরায় ব্যবহার: একবার ক্লাস লিখলে তা দিয়ে হাজার হাজার অবজেক্ট বানানো যায়।
গুছিয়ে রাখা: বড় প্রোজেক্টে এলোমেলো কোড না লিখে ক্লাসের মাধ্যমে সবকিছু আলাদা রাখা যায়।
বাস্তবসম্মত সমাধান: বাস্তব জগতের জিনিস (যেমন: ছাত্র, গাড়ি, ব্যাংক অ্যাকাউন্ট) কোডিংয়ে প্রকাশ করা সহজ হয়
'''

'''
হ্যাঁ, একদম ঠিক ধরেছেন! ১ম লাইনে def __len__(self): লিখে আপনি পাইথনকে এই সংকেত দিচ্ছিলেন যে— "এই ক্লাসের কোনো অবজেক্টের দৈর্ঘ্য (length) মাপার জন্য এই অংশটুকু ব্যবহার করো।"
সহজ কথায়, সে তখন নিচের কাজগুলো করার জন্য প্রস্তুত ছিল:
১. len() ফাংশনের জবাব দেওয়া: কেউ যদি ভবিষ্যতে len(myobj) লিখে কল করে, তবে তার উত্তর এই ফাংশন থেকেই আসবে।
২. অবজেক্টের 'Truthiness' নির্ধারণ করা: অবজেক্টটি কি "ভর্তি" নাকি "খালি"? যদি দৈর্ঘ্য ০ হয়, তবে পাইথন বুঝবে এটি False। আর দৈর্ঘ্য ০-এর বেশি হলে বুঝবে এটি True।
'''

# ফাংশন বুলিয়ান রিটার্ন করতে পারে।
# উদাহরণ:
# একটি ফাংশনের উত্তর প্রিন্ট করুন:
