# উদাহরণ
# 'banana' এবং 'cherry' মান দুটিকে 'blackcurrant' এবং 'watermelon' মান দিয়ে পরিবর্তন করুন:

thislist = ["apple", "banana", "cherry", "orange", "kiwi", "mango"]
