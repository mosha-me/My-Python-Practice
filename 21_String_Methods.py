# 34. rjust()	  Returns a right justified version of the string

# rjust() (Right Justify) মেথডটি ljust() এর ঠিক উল্টো কাজ করে। এটি আপনার স্ট্রিংটিকে একটি নির্দিষ্ট দৈর্ঘ্যের ভেতরে ডান দিকে সাজায় এবং বাম পাশের খালি জায়গাগুলো কোনো অক্ষর বা স্পেস দিয়ে পূরণ করে।
'''
সহজ কথায়,
আপনি যদি চান আপনার লেখাটি ডান পাশ ঘেঁষে থাকুক, তবে এটি ব্যবহার করবেন।সিনট্যাক্স:pythonstring.rjust(length, character)
'''
# উদাহরণ দেখে নেওয়া যাক:
# ১. ডিফল্ট স্পেস ব্যবহার করে:এটি স্ট্রিংয়ের বাম পাশে খালি জায়গা তৈরি করে লেখাটিকে ডানে সরিয়ে দেয়।
text = "Python"
# ২০ দৈর্ঘ্যের মধ্যে ডানে সাজানো
print(text.rjust(20)) 
# আউটপুট: "              Python"

# ২. নির্দিষ্ট ক্যারেক্টার দিয়ে পূরণ করা:খালি জায়গাগুলো স্পেসের বদলে অন্য কিছু (যেমন ডট বা জিরো) দিয়ে ভরপুর করা যায়।
text = "Apple"
# ১০ দৈর্ঘ্যের মধ্যে ডানে রেখে বাকিটা '*' দিয়ে পূরণ
print(text.rjust(10, "*"))
# আউটপুট: "*****Apple"

# কেন এটি ব্যবহার করবেন?
# এটি সবচেয়ে বেশি কাজে লাগে যখন আপনি সংখ্যার তালিকা বা টাকার হিসাব কলাম আকারে প্রিন্ট করতে চান, যাতে সব সংখ্যা ডান দিক থেকে সমান লাইনে থাকে (যেমন রসিদ বা ইনভয়েস)।

# উদাহরণ (হিসাব মেলানো):
print("Price:".ljust(10), "500".rjust(5))
print("Tax:".ljust(10), "50".rjust(5))
# আউটপুট:
# Price:       500
# Tax:          50

