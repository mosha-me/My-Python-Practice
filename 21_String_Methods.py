# আউটপুট: *******Hello********

''' মনে রাখার মতো বিষয়:
১. দৈর্ঘ্য: আপনার দেয়া দৈর্ঘ্য যদি স্ট্রিংয়ের নিজের দৈর্ঘ্যের চেয়ে কম হয়, তবে এটি কোনো পরিবর্তন করবে না। 
২. হিসাব: যদি খালি জায়গা বিজোড় সংখ্যার হয় (যেমন একদিকে ৭টি এবং অন্যদিকে ৮টি স্পেস), তবে পাইথন নিজে থেকেই তা ব্যালেন্স করে নেয়।
'''
# center() মেথডে দৈর্ঘ্য (length) এবং হিসাব (alignment) নিয়ে মূলত ৩টি সাধারণ সমস্যা বা কনফিউশন হতে পারে।

# চলুন সহজ উদাহরণ দিয়ে সেগুলো বুঝে নিই:

''' ১. স্ট্রিংয়ের চেয়ে ছোট দৈর্ঘ্য দিলে (No Change)
আপনি যদি width বা দৈর্ঘ্য হিসেবে এমন একটি সংখ্যা দেন যা আপনার স্ট্রিংয়ের আসল দৈর্ঘ্যের চেয়ে ছোট বা সমান, তবে পাইথন কোনো পরিবর্তন করবে না। এটি কোনো এরর দেবে না, কিন্তু টেক্সটটি সেন্টারেও আসবে না। '''

text = "Python" # এখানে দৈর্ঘ্য ৬
print(text.center(4)) 
# আউটপুট: 'Python' (কোনো পরিবর্তন হয়নি, কারণ ৪ < ৬)

''' ২. বিজোড় স্পেসের হিসাব (Uneven Padding)
সবচেয়ে বেশি কনফিউশন হয় যখন খালি জায়গার সংখ্যা বিজোড় (Odd) হয়। যেমন ধরুন, আপনার কাছে ১টি স্পেস আছে, এখন সেটি কি বামে বসবে নাকি ডানে?পাইথনের নিয়ম হলো: যদি দুই পাশে সমান সমান স্পেস বসানো না যায়, তবে বামে ১টি স্পেস কম এবং ডানে ১টি স্পেস বেশি থাকে। '''

text = "Hi" # দৈর্ঘ্য ২
# আমরা চাই দৈর্ঘ্য ৫ (তাহলে বাকি থাকে ৩টি স্পেস)
print(text.center(5, "*"))
# আউটপুট: '*Hi**' 
# (বামে ১টি '*', ডানে ২টি '*')

''' ৩. শুধুমাত্র একটি ক্যারেক্টার ব্যবহার করা (Fill Character Limit)
center() মেথডে আপনি চাইলে * বা - দিয়ে ঘর ভরাট করতে পারেন, কিন্তু আপনি সেখানে একের অধিক অক্ষর (যেমন: "--") ব্যবহার করতে পারবেন না। করলে পাইথন TypeError দেবে। '''

text = "Hello"
print(text.center(20, "*-*")) # এটি Error দেবে!
