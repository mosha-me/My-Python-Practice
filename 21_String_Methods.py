# 8. find()	  Searches the string for a specified value and returns the position of where it was found

# পাইথনে find() মেথডটি একটি স্ট্রিংয়ের ভেতর নির্দিষ্ট কোনো শব্দ বা অক্ষর খুঁজে বের করার জন্য ব্যবহার করা হয়। এটি ওই কাঙ্ক্ষিত অংশটি স্ট্রিংয়ের কত নম্বর পজিশন বা ইন্ডেক্স (index) থেকে শুরু হয়েছে, সেই সংখ্যাটি আমাদের জানায়।

''' এটি কিভাবে কাজ করে?
মেথডটি স্ট্রিংয়ের শুরু থেকে খোঁজা শুরু করে এবং প্রথম যেখানে মিল পায়, সেই ইনডেক্স নম্বরটি রিটার্ন করে। যদি কাঙ্ক্ষিত শব্দটি স্ট্রিংয়ের ভেতর না পাওয়া যায়, তবে এটি -1 রিটার্ন করে। '''

# সিনট্যাক্স:pythonstring.find(value, start, end)
# প্যারামিটারসমূহ:
# value (আবশ্যক): যা আপনি খুঁজছেন।
# start (ঐচ্ছিক): যেখান থেকে খোঁজা শুরু করবেন (ডিফল্ট ০)।end (ঐচ্ছিক): কত নম্বর ইনডেক্স পর্যন্ত খুঁজবেন।

# উদাহরণ:
pythontext = "Hello, welcome to Python world."

