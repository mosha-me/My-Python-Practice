# সহজ কথায়,
# এটি দেখে যে স্ট্রিংয়ের প্রতিটি শব্দের প্রথম অক্ষর বড় হাতের (Upper Case) এবং বাকি অক্ষরগুলো ছোট হাতের (Lower Case) কি না।

'''এটি যেভাবে কাজ করে:
True দিবে: যদি প্রতিটি শব্দের প্রথম অক্ষর বড় হাতের হয় এবং পরেরগুলো ছোট হাতের হয়।
False দিবে: যদি কোনো শব্দের প্রথম অক্ষর ছোট হাতের হয় অথবা বড় হাতের অক্ষরের পরে আবার কোনো বড় হাতের অক্ষর থাকে।
'''
# উদাহরণ:
pythontxt1 = "Hello World"
print(txt1.istitle())  # আউটপুট: True (প্রতিটি শব্দের শুরু বড় হাতের)

txt2 = "Hello world"
print(txt2.istitle())  # আউটপুট: False (world এর 'w' ছোট হাতের)

txt3 = "PYTHON"
print(txt3.istitle())  # আউটপুট: False (সবগুলোই বড় হাতের)

txt4 = "22 Names"
print(txt4.istitle())  # আউটপুট: True (সংখ্যা থাকলেও অক্ষরের নিয়ম ঠিক আছে)

# একটি মজার ব্যাপার:
# যদি শব্দের শুরুতে কোনো সংখ্যা বা চিহ্ন থাকে, তবে এটি তার ঠিক পরের প্রথম অক্ষরটি বড় হাতের কি না তা চেক করে।

