text = "apple, banana, cherry"
print(text.rsplit(", ")) 
# আউটপুট: ['apple', 'banana', 'cherry']

# ২. maxsplit ব্যবহার করে (আসল জাদু এখানে):ধরা যাক, আপনার অনেকগুলো শব্দ আছে কিন্তু আপনি শুধু ডান দিক থেকে ১টি শব্দ আলাদা করতে চান।
text = "apple, banana, cherry, orange"
# ডান দিক থেকে মাত্র ১ বার ভাগ করা
