# 4. count()	Returns the number of times a specified value occurs in a string

''' count() মেথডটি অনেকটা গোয়েন্দার মতো কাজ করে। এটি একটি বড় টেক্সটের ভেতর থেকে আপনার দেওয়া কোনো নির্দিষ্ট শব্দ বা অক্ষর কতবার আছে তা গুনে বের করে দেয়। '''

''' সহজ উদাহরণ:
ধরা যাক, একটি লাইনে কতবার "apple" আছে তা আমরা জানতে চাই। '''

text = "I love apple, apple is my favorite fruit"
result = text.count("apple")
print(result) 
# আউটপুট: 2

# এটি ব্যবহারের ৩টি মজার নিয়ম:
''' ১. বড় হাত-ছোট হাতের পার্থক্য (Case Sensitive):পাইথনের কাছে "Apple" এবং "apple" এক নয়। তাই খোঁজার সময় এটি খেয়াল রাখতে হয় '''

text = "Apple apple APPLE"
print(text.count("apple")) 
# আউটপুট: 1 (শুধুমাত্র ছোট হাতেরটি গুনেছে)

''' ২. নির্দিষ্ট অংশ থেকে খোঁজা (Start and End):আপনি চাইলে পুরো টেক্সট না খুঁজে নির্দিষ্ট একটি সীমানা (শুরু এবং শেষ) বলে দিতে পারেন। '''

text = "banana banana banana"
# ১০ নম্বর পজিশন থেকে খোঁজা শুরু করো
print(text.count("banana", 10)) 
# আউটপুট: 1

''' ৩. ওভারল্যাপিং বা একটার ওপর আরেকটা (Overlapping):এটি কোনো অক্ষরকে একবার গুনে ফেললে পরের বার আর সেটাকে ব্যবহার করে না। '''

text = "aaaaa"
print(text.count("aa"))
# আউটপুট: 2 (৩ নয়, কারণ প্রথম 'aa' গোনার পর পরের 'aa' খোঁজে)

''' এটা বুঝতে পারা একটু ট্রিকি, তবে নিচের ছবির মতো চিন্তা করলে সহজ হয়ে যাবে।
পাইথন যখন count() করে, তখন সে বাম থেকে ডানে গুনতে থাকে। একবার কোনো অক্ষর গুনে ফেললে, সেটিকে আর দ্বিতীয়বার ব্যবহার করে না (অর্থাৎ Overlapping করে না)। '''

# আসুন আপনার উদাহরণটি ভাঙি: text = "aaaaa" (এখানে ৫টি 'a' আছে)
'''
১. পাইথন প্রথমে প্রথম দুটি 'aa' খুঁজে পেল। (বাকি রইল ৩টি 'a')
২. এরপর সে তার পরের অংশ থেকে খোঁজা শুরু করল এবং আরও একটি 'aa' পেল। (বাকি রইল ১টি 'a')
৩. শেষে মাত্র ১টি 'a' বাকি আছে, যা দিয়ে আর 'aa' বানানো সম্ভব নয়।
'''
# সহজভাবে দেখলে:
# [aa][aa]a -> এখানে জোড়া হয়েছে মাত্র ২টি। মাঝখানের 'a' গুলোকে সে আগের জোড়ায় ব্যবহার করে ফেলেছে বলে নতুন জোড়া বানাতে পারছে না।

# এখানে "banana" এর ভেতর "ana" দুইবার দেখা গেলেও (b[ana]na এবং ban[ana]), পাইথন প্রথম "ana" নেওয়ার পর বাকি থাকে শুধু "na", তাই সে ২বার গুনতে পারে না।

