# 18. islower()	Returns True if all characters in the string are lower case

# islower() মেথডটি চেক করে আপনার স্ট্রিংয়ের ভেতরে থাকা সব অক্ষর ছোট হাতের (lower case) কি না।এটি মূলত আপনার টেক্সটের ক্যারেক্টারগুলোর অবস্থা যাচাই করে নিচের নিয়ম অনুযায়ী:
'''
১. True দিবে যদি: স্ট্রিংয়ে অন্তত একটি অক্ষর থাকে এবং সেই সব কয়টি অক্ষর ছোট হাতের হয়।
২. False দিবে যদি: স্ট্রিংয়ে অন্তত একটি বড় হাতের (UPPER CASE) অক্ষর থাকে।
৩. অন্যান্য চিহ্ন: স্ট্রিংয়ে যদি সংখ্যা, স্পেস বা স্পেশাল ক্যারেক্টার থাকে, তবে সেগুলোকে এটি এড়িয়ে যায় (ইগনোর করে), শুধু অক্ষরগুলো চেক করে।
'''
# উদাহরণ দেখে নেওয়া যাক:
pythontxt1 = "hello world"
print(txt1.islower())  # আউটপুট: True (সব অক্ষর ছোট হাতের)

txt2 = "Hello world"
print(txt2.islower())  # আউটপুট: False (শুরুতে 'H' বড় হাতের)

txt3 = "hello 123!"
print(txt3.islower())  # আউটপুট: True (সংখ্যা ও চিহ্ন থাকলেও অক্ষরগুলো ছোট হাতের)

txt4 = "123"
print(txt4.islower())  # আউটপুট: False (কারণ এখানে কোনো অক্ষরই নেই)

# বাস্তব ক্ষেত্রে ব্যবহার:
# ধরুন, আপনি ইউজারের কাছ থেকে পাসওয়ার্ড বা ইমেইল ইনপুট নিচ্ছেন এবং আপনি চাচ্ছেন সব যেন ছোট হাতের অক্ষরে থাকে, তখন এটি দিয়ে চেক করতে পারেন।

