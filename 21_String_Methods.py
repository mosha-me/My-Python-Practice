# 20. isprintable()	 Returns True if all characters in the string are printable

# isprintable() মেথডটি চেক করে আপনার স্ট্রিংয়ের ভেতরে থাকা সব ক্যারেক্টার মুদ্রণযোগ্য (Printable) কি না।

# সহজ কথায়,
# কোনো স্ট্রিং প্রিন্ট করলে যদি স্ক্রিনে তা দেখা যায়, তবে সেটি printable।কিন্তু কিছু ক্যারেক্টার আছে যা স্ক্রিনে সরাসরি দেখা যায় না, বরং সেগুলো কম্পিউটারকে কোনো নির্দেশ দেয় (যেমন: নতুন লাইন তৈরি করা বা ট্যাব দেওয়া)। এগুলোকে বলা হয় Non-printable ক্যারেক্টার।
'''
এটি যেভাবে কাজ করে:
True দিবে: যদি স্ট্রিংয়ের সব ক্যারেক্টার অক্ষর, সংখ্যা, স্পেস বা বিরামচিহ্ন হয়।
False দিবে: যদি স্ট্রিংয়ে এমন কোনো ক্যারেক্টার থাকে যা স্ক্রিনে দেখা যায় না (যেমন: \n বা New Line, \t বা Tab, \r ইত্যাদি)।
'''
# উদাহরণ:
python# সাধারণ টেক্সট (Printable)
txt1 = "Hello! 123"
print(txt1.isprintable())  # আউটপুট: True

# নতুন লাইন বা ট্যাব যুক্ত টেক্সট (Non-printable)
txt2 = "Hello\nWorld"
print(txt2.isprintable())  # আউটপুট: False (কারণ \n দেখা যায় না, শুধু লাইন ভেঙে দেয়)

txt3 = "Check\tTab"
print(txt3.isprintable())  # আউটপুট: False (কারণ \t একটি ট্যাব স্পেস দেয়)

# খালি স্ট্রিং (Printable)
print(" ".isprintable())    # আউটপুট: True


