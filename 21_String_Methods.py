# 32. rfind()	Searches the string for a specified value and returns the last position of where it was found

# rfind() (Reverse Find) মেথডটি find() এর মতোই কাজ করে, কিন্তু এটি স্ট্রিংয়ের শুরু থেকে না খুঁজে শেষ দিক থেকে খোঁজা শুরু করে। এটি নির্দিষ্ট শব্দ বা অক্ষরটি স্ট্রিংয়ের সবচেয়ে শেষে যেখানে আছে, সেই পজিশন বা ইনডেক্স নম্বরটি রিটার্ন করে।
'''
এটি যেভাবে কাজ করে:
এটি স্ট্রিংয়ের শেষ থেকে খোঁজা শুরু করলেও ইনডেক্স নম্বর কিন্তু শুরু (০) থেকেই গণনা করে।যদি শব্দটি একাধিকবার থাকে, তবে এটি সবচেয়ে ডানের (শেষের) পজিশনটি দিবে।যদি শব্দটি খুঁজে না পায়, তবে এটি -1 রিটার্ন করবে।
'''
# উদাহরণ দেখে নেওয়া যাক:
text = "Apple, Banana, Apple, Cherry"

# সাধারণ find() কি দেয়? (প্রথমবার যেখানে পেয়েছে)
print(text.find("Apple"))   # আউটপুট: 0

# rfind() কি দেয়? (শেষবার যেখানে পেয়েছে)
print(text.rfind("Apple"))  # আউটপুট: 15

# প্যারামিটারসমূহ:
# string.rfind(value, start, end)আপনি চাইলে একটি নির্দিষ্ট রেঞ্জ (start থেকে end) এর মধ্যেও শেষ দিক থেকে খুঁজতে পারেন।

# বাস্তব উদাহরণ:
# ধরা যাক আপনার কাছে একটি ফাইলের পাথ (Path) আছে এবং আপনি শুধু ফাইলটির নাম আলাদা করতে চান। তখন শেষের স্ল্যাশ (/) খুঁজে বের করতে এটি খুব কাজে লাগে:
path = "C:/users/documents/resume.pdf"
index = path.rfind("/")
file_name = path[index+1:]
print(file_name) # আউটপুট: resume.pdf

