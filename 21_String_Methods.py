# 2. casefold()	 Converts string into lower case

''' পাইথনে casefold() মেথডটিও lower() মেথডের মতো স্ট্রিংকে ছোট হাতের অক্ষরে (lowercase) রূপান্তর করে। 
তবে এটি সাধারণ lower() এর চেয়ে অনেক বেশি শক্তিশালী এবং আক্রমণাত্মক (aggressive)। 
এটি প্রধানত দুটি স্ট্রিংয়ের মধ্যে নিখুঁত তুলনা করার জন্য ব্যবহৃত হয় '''

# এটি কিভাবে কাজ করে?
# সাধারণ ইংরেজি অক্ষরের ক্ষেত্রে lower() এবং casefold() একই ফলাফল দেয়। কিন্তু অন্যান্য ভাষার বিশেষ কিছু অক্ষরের ক্ষেত্রে casefold() সেই অক্ষরগুলোকে ভেঙে সাধারণ ছোট হাতের অক্ষরে রূপান্তর করতে পারে যা lower() পারে না |

# একটি চমৎকার উদাহরণ:
# জার্মান ভাষায় একটি অক্ষর আছে 'ß' (যাকে Caseless Matching-এ 'ss' ধরা হয়)।

text = "groß"
print(text.lower())    # আউটপুট: groß (অক্ষরটি পরিবর্তন হয়নি)
print(text.casefold()) # আউটপুট: gross (এটি 'ß' কে 'ss' এ রূপান্তর করেছে)

# কেন casefold() ব্যবহার করবেন?
# যখন আপনি ব্যবহারকারীর কাছ থেকে পাওয়া কোনো ইনপুটকে অন্য একটি স্ট্রিংয়ের সাথে তুলনা করবেন এবং নিশ্চিত হতে চাইবেন যে ভাষার পার্থক্যের কারণে যেন কোনো ভুল না হয়, তখন casefold() ব্যবহার করা সবচেয়ে নিরাপদ

# তুলনার উদাহরণ:

txt1 = "groß"
txt2 = "GROSS"

# lower() দিয়ে তুলনা করলে False আসবে
print(txt1.lower() == txt2.lower()) # False

# casefold() দিয়ে তুলনা করলে True আসবে
print(txt1.casefold() == txt2.casefold()) # True


# সহজ কথায়, সাধারণ ইংরেজি টেক্সট হলে আপনি lower() ব্যবহার করতে পারেন, কিন্তু আন্তর্জাতিক বা বহুভাষিক টেক্সট নিয়ে কাজ করলে Case-Insensitive Comparison-এর জন্য Python String casefold() ব্যবহার করাই বুদ্ধিমানের কাজ। 

