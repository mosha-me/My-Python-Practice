# 6. endswith()	   Returns true if the string ends with the specified value

''' পাইথনে endswith() মেথডটি কোনো একটি স্ট্রিং নির্দিষ্ট কোনো শব্দ বা অক্ষর (suffix) দিয়ে শেষ হয়েছে কিনা তা যাচাই করার জন্য ব্যবহৃত হয়। এটি মূলত একটি Boolean মান প্রদান করে '''

# —যদি স্ট্রিংটি ঐ নির্দিষ্ট মান দিয়ে শেষ হয় তবে এটি True রিটার্ন করে, অন্যথায় False দেয়।

''' এটি কিভাবে কাজ করে?
মেথডটি আপনার দেওয়া স্ট্রিং-এর শেষ অংশটিকে নির্দিষ্ট ভ্যালুর সাথে মিলিয়ে দেখে। যদি হুবহু মিলে যায়, তবেই এটি সফল হয়।
'''
# সিনট্যাক্স:
pythonstring.endswith(value, start, end)

'''
প্যারামিটারসমূহ:
value (আবশ্যক): যে টেক্সট বা অক্ষরটি দিয়ে স্ট্রিং শেষ হয়েছে কিনা আপনি পরীক্ষা করতে চান। এটি একটি tuple (একাধিক মান) হতে পারে, সেক্ষেত্রে যেকোনো একটির সাথে মিললে এটি True দেখাবে।
start (ঐচ্ছিক): যে ইনডেক্স থেকে তল্লাশি শুরু হবে।
end (ঐচ্ছিক): যে ইনডেক্স পর্যন্ত তল্লাশি চলবে।
'''
