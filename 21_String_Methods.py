# 17. isidentifier()	Returns True if the string is an identifier

# পাইথনে isidentifier() মেথডটি চেক করে যে আপনার স্ট্রিংটি একটি বৈধ আইডেন্টিফায়ার (Valid Identifier) কি না।

''' সহজ কথায়,
আপনি যদি পাইথনে কোনো ভেরিয়েবল (Variable), ফাংশন (Function), বা ক্লাসের (Class) নাম দিতে চান, তবে সেই নামটি পাইথনের নিয়ম অনুযায়ী সঠিক কি না, তা এই মেথড দিয়ে যাচাই করা যায়। '''

'''
পাইথনে আইডেন্টিফায়ার হওয়ার নিয়মগুলো কী?
১. এটি শুধুমাত্র অক্ষর (A-Z, a-z), সংখ্যা (0-9) এবং আন্ডারস্কোর (_) দিয়ে গঠিত হতে পারে।
২. এটি কখনোই সংখ্যা দিয়ে শুরু হতে পারবে না।
৩. এর মাঝে কোনো স্পেস (Space) বা স্পেশাল ক্যারেক্টার (যেমন: @, #, -) থাকতে পারবে না।
'''
# উদাহরণসহ ব্যবহার:
# বৈধ নাম
print("my_variable".isidentifier())  # আউটপুট: True
print("variable123".isidentifier()) # আউটপুট: True
print("_temp".isidentifier())        # আউটপুট: True

# অবৈধ নাম
print("123variable".isidentifier())  # আউটপুট: False (সংখ্যা দিয়ে শুরু হয়েছে)
print("my variable".isidentifier())  # আউটপুট: False (মাঝখানে স্পেস আছে)
print("my-variable".isidentifier())  # আউটপুট: False (ড্যাশ ব্যবহার করা হয়েছে)

# একটি ছোট টিপস:
# আপনার দেওয়া স্ট্রিংটি যদি পাইথনের কোনো Keyword (যেমন: def, class, if) হয়, তবুও isidentifier() কিন্তু True রিটার্ন করবে। কারণ টেকনিক্যালি সেগুলো আইডেন্টিফায়ার হিসেবে গঠনগতভাবে সঠিক। 
# তবে আপনি সেগুলোকে নিজের ভেরিয়েবল নাম হিসেবে ব্যবহার করতে পারবেন না।

