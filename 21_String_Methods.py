# 39. splitlines()	 Splits the string at line breaks and returns a list

# splitlines() মেথডটি একটি স্ট্রিংকে তার লাইন ব্রেক (Line Breaks) বা নতুন লাইনের (\n, \r) ওপর ভিত্তি করে ভাগ করে একটি লিস্ট (List) তৈরি করে।
'''
সহজ কথায়, 
আপনার টেক্সটে যদি অনেকগুলো লাইন থাকে, তবে প্রতিটি লাইনকে আলাদা আলাদা করার জন্য এটি ব্যবহার করা হয়।
'''
'''
এটি যেভাবে কাজ করে:
এটি স্ট্রিংয়ের ভেতরে থাকা \n (Newline), \r (Carriage Return), বা \r\n চিহ্নগুলো খুঁজে বের করে এবং সেখান থেকে টেক্সটটিকে আলাদা করে দেয়।
'''
# সিনট্যাক্স:pythonstring.splitlines(keepends)

# উদাহরণ:
# ১. সাধারণ ব্যবহার:
text = "Hello World\nWelcome to Python\nLearning is fun"
result = text.splitlines()
print(result)
# আউটপুট: ['Hello World', 'Welcome to Python', 'Learning is fun']

# ২. keepends প্যারামিটার:ডিফল্টভাবে এই মেথডটি লাইনের শেষের \n চিহ্নটি ফেলে দেয়। কিন্তু আপনি যদি চান চিহ্ণটি লিস্টের ভেতরেই থাকুক, তবে True লিখে দিতে পারেন।pythontext = "Line 1\nLine 2"
print(text.splitlines(True))
# আউটপুট: ['Line 1\n', 'Line 2']

'''
split("\n") এবং splitlines() এর মধ্যে পার্থক্য কী?

split("\n") শুধুমাত্র একটি নির্দিষ্ট \n চিহ্ন খোঁজে। যদি স্ট্রিংয়ের শেষে \n থাকে, তবে সে শেষে একটি বাড়তি খালি আইটেম (Empty string) তৈরি করে ফেলে।
splitlines() স্মার্টলি সব ধরণের লাইন ব্রেক হ্যান্ডেল করে এবং শেষে বাড়তি খালি আইটেম তৈরি করে না।

বাস্তব ক্ষেত্রে ব্যবহার:
যখন আপনি কোনো বড় টেক্সট ফাইল পড়েন এবং প্রতিটি লাইন নিয়ে আলাদা আলাদা কাজ করতে চান, তখন splitlines() সবচেয়ে কার্যকর| 

split("\n") এবং splitlines() এর মধ্যে প্রধান পার্থক্য হলো তারা লাইনের শেষের অংশ এবং বিভিন্ন ধরণের লাইন ব্রেক কিভাবে হ্যান্ডেল করে।
নিচের উদাহরণটি দেখলে বিষয়টি একদম পরিষ্কার হয়ে যাবে:
'''
