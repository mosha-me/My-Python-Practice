# 33. rindex()	  Searches the string for a specified value and returns the last position of where it was found

# rindex() মেথডটি একদম rfind() এর মতোই কাজ করে—এটি স্ট্রিংয়ের শেষ দিক থেকে (ডান দিক থেকে) নির্দিষ্ট কোনো শব্দ বা অক্ষর খুঁজে বের করে এবং তার ইনডেক্স নম্বর রিটার্ন করে।তবে এখানেও সেই একই সতর্কতা আছে যা আমরা find() এবং index() এর ক্ষেত্রে দেখেছিলাম।
'''
এটি যেভাবে কাজ করে:
১. এটি স্ট্রিংয়ের শেষ থেকে খোঁজা শুরু করে এবং কাঙ্ক্ষিত বস্তুটি শেষবার যেখানে আছে, সেই পজিশনটি জানায়।
২. যদি শব্দটি খুঁজে না পাওয়া যায়, তবে এটি একটি ValueError (এরর) দেয় এবং আপনার প্রোগ্রামটি বন্ধ হয়ে যাবে।
'''
# উদাহরণ:
text = "I love Python, Python is great"

# 'Python' শব্দটিকে শেষবার কোথায় পাওয়া গেছে?
print(text.rindex("Python"))  # আউটপুট: 15

# সাধারণ index() দিলে আসত: 7 (কারণ সেটি প্রথমবার যেখানে পেয়েছে)
'''
rfind() এবং rindex() এর পার্থক্য:
rfind(): যদি খুঁজে না পায়, তবে -1 রিটার্ন করে (এটি বেশি নিরাপদ)।
rindex(): যদি খুঁজে না পায়, তবে Error দেয়।
'''
# উদাহরণ (খুঁজে না পেলে):
text = "Hello World"
print(text.rfind("Java"))   # আউটপুট: -1
print(text.rindex("Java"))  # আউটপুট: ValueError: substring not found

# কেন এটি ব্যবহার করবেন?
# যখন আপনি নিশ্চিত যে শব্দটি স্ট্রিংয়ে আছে এবং আপনি একদম শেষের পজিশনটি চাচ্ছেন, তখনই এটি ব্যবহার করা হয়। যেমন: কোনো ইমেইল অ্যাড্রেসে শেষ ডট (.) টি কোথায় আছে তা জানতে।

