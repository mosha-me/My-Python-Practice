# 21. isspace()	 Returns True if all characters in the string are whitespaces

# isspace() মেথডটি চেক করে আপনার স্ট্রিংটি শুধুমাত্র হোয়াইটস্পেস (Whitespace) বা ফাঁকা জায়গা দিয়ে তৈরি কি না।
"""
যদি স্ট্রিংয়ের ভেতরে কোনো অক্ষর, সংখ্যা বা চিহ্ন থাকে, তবে এটি সরাসরি False দিয়ে দেয়।
এটি তখনই True হবে যখন পুরো স্ট্রিংটিতে শুধু ফাঁকা জায়গা থাকবে।
এটি কোন কোন জিনিসকে 'Whitespace' হিসেবে ধরে?
১. সাধারণ স্পেস (Space: " ")
২. ট্যাব (Tab: \t)
৩. নতুন লাইন (New Line: \n)
৪. ক্যারেজ রিটার্ন (Carriage Return: \r)
"""
# উদাহরণ দেখে নেওয়া যাক:
pythontxt1 = "   "
print(txt1.isspace())  # আউটপুট: True (সবগুলোই স্পেস)

txt2 = "  A  "
print(txt2.isspace())  # আউটপুট: False (মাঝখানে 'A' অক্ষরটি আছে)

txt3 = "\n\t "
print(txt3.isspace())  # আউটপুট: True (নিউ লাইন এবং ট্যাবকেও এটি হোয়াইটস্পেস ধরে)

# বাস্তব ক্ষেত্রে এর ব্যবহার:
# যখন কোনো ইউজার ইনপুট ফিল্ডে কিছু না লিখে শুধু স্পেস দিয়ে ফরম পূরণ করতে চায়, তখন এটি ব্যবহার করে আপনি সেই ইনপুটটি বাতিল করতে পারেন।

